    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        /////////////////////////////////////////
        String Fname;
        String Lname;
        String phone;
        String email;
        String address;
        //String cIn_date;
        //String cOut_date;
        String Id_type;
        String ID_no;

        if(this.Cust_Fname.getText().equals("")){
            
            Cust_Fname.setBackground(new Color(255,150,150));

        }
        else if(this.Cust_Lname.getText().equals("")){
            Cust_Lname.setBackground(new Color(255,150,150));
            
        }
        else if(this.Cust_Phone.getText().equals("")){
            Cust_Phone.setBackground(new Color(255,150,150));
            
        }
        else if(this.Cust_Email.getText().equals("")){
            Cust_Email.setBackground(new Color(255,150,150));
            
        }
        else if(this.Cust_Address.getText().equals("")){
            Cust_Address.setBackground(new Color(255,150,150));
            
        }
        else if(this.Cust_ID_No.getText().equals("")){
            Cust_ID_No.setBackground(new Color(255,150,150));
            
        }
        else{
            Fname=this.Cust_Fname.getText();
            Lname=this.Cust_Lname.getText();
            phone=this.Cust_Phone.getText();
            email=this.Cust_Email.getText();
            address=this.Cust_Address.getText();
            Id_type=this.Cust_ID_Type.getSelectedItem().toString();
            ID_no=this.Cust_ID_No.getText();            
            //cIn_date=this.Cust_CheckIn_Date.getDate().toString();
            //cOut_date=this.Cust_CheckOut_Date.getDate().toString();
            
            try{
                PreparedStatement pst = con.prepareStatement("insert into customer(fname,lname,phone,email,address,ID_type,ID_no) values(?,?,?,?,?,?,?)");
                pst.setString(1,Fname);
                pst.setString(2,Lname);
                pst.setString(3,phone);
                pst.setString(4,email);
                pst.setString(5,address);
                pst.setString(6,Id_type);
                pst.setString(7,ID_no);
                
                int i=pst.executeUpdate();
                if(i>0){
                    JOptionPane.showMessageDialog(null, "Record Sucssefully added!");
                }
                    
                }
            catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
        }
        /////////////////////////////////////////
    }//GEN-LAST:event_jButton1ActionPerformed

