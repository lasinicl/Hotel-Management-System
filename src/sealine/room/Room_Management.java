    private void Cust_ID_NoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_Cust_ID_NoFocusLost
        // TODO add your handling code here:
        /////////////////////////////////////////
        String s=Cust_ID_No.getText();
        String t=Cust_ID_Type.getSelectedItem().toString();

        int num=s.length();
        if(t.equals("National Identity Card")){
            if(num>12){
                JOptionPane.showMessageDialog(null,"NIC Number has only 10 digits!!","Invalid Data", JOptionPane.ERROR_MESSAGE);
                Cust_ID_No.setText("");
            }
        }

        if(t.equals("Passport Number")){
            if(!(s.startsWith("M-LKAN")||s.startsWith("N-LKAN")||s.startsWith("OL-LKAN")||s.startsWith("D-LKA"))&&(s.length()==13)&&(!s.matches("^.*[^a-zA-Z0-9 ].*$"))){
                JOptionPane.showMessageDialog(null,"Enter Valid Pass Port number!","Invalid Data", JOptionPane.ERROR_MESSAGE);
                Cust_ID_No.setText("");
            }
        }

        if(t.equals("Driver Lisence")){
            if((s.length()!=7)){
                JOptionPane.showMessageDialog(null,"Enter Valid Lisence number!","Invalid Data", JOptionPane.ERROR_MESSAGE);
                Cust_ID_No.setText("");
            }

        }
        //////////////////////////////////////////
    }//GEN-LAST:event_Cust_ID_NoFocusLost

